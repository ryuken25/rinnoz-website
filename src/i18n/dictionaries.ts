export const dictionaries: Record<Locale, Dict> = { id, en };

export function getArray<T = string>(locale: Locale, key: string): T[] {
  const value = key.split('.').reduce<unknown>((acc, part) => {
    if (acc && typeof acc === 'object' && part in (acc as Record<string, unknown>)) return (acc as Record<string, unknown>)[part];
    return undefined;
  }, dictionaries[locale]);
  return Array.isArray(value) ? (value as T[]) : [];
}
